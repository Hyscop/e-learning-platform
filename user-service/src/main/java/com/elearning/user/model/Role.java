package com.elearning.user.model;

/**
 * USER ROLES - Defines access levels in the platform
 */

public enum Role {

    /**
     * STUDENT - Default role for learners
     */

    STUDENT,

    /**
     * INSTRUCTOR - Role for course creators. Content creators and teachers.
     */

    INSTRUCTOR,

    /**
     * ADMIN - Full access to manage platform
     */

    ADMIN

}
